def main(input):
    # Конвертируем входную строку в число
    num = int(input)
    # Извлекаем поля
    C4 = (num >> 16) & ((1 << 4) - 1)  
    C3 = (num >> 10) & ((1 << 6) - 1)  
    C2 = (num >> 7) & ((1 << 3) - 1)   
    C1 = num & ((1 << 7) - 1)          
    # Переставляем битовые поля
    output_num = (C2 << 17) | (C4 << 13) | (C3 << 7) | C1
    # Возвращаем результат в виде строки
    return str(output_num)
# Тесты
print(main('743096'))  # Ожидаемый результат: '748216'
print(main('42860'))   # Ожидаемый результат: '791788'
